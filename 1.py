# This program performs basic arithmetic operations on two numbers provided by the user.
# It calculates and prints the sum, difference, product, quotient, remainder, floor quotient,


n1 = int(input("Enter 1st number: "))
n2 = int(input("Enter 2nd number: "))
print("The sum is ",n1+n2)
print("The difference is ",n1-n2)
print("the product is ",n1*n2)
print("the quotient is ",n1/n2)
print("the remainder is ",n1%n2)
print("the power is ",n1**n2)
print("the average is ",(n1+n2)/2)
print("the maximum is ",max(n1,n2))
print("the minimum is ",min(n1,n2))

